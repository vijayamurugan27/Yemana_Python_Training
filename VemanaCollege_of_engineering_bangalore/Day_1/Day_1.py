def add_numbers(a, b):
    '''
    This function takes two numbers as input and returns their sum.

    Args:
        a (float): The first number to be added.
        b (float): The second number to be added.

    Returns:
        float: The sum of the two input numbers.

    Example:
        >>> add_numbers(5, 3)
        8
        >>> add_numbers(-2.5, 1.5)
        -1.0
    '''
    result = a + b
    return result
